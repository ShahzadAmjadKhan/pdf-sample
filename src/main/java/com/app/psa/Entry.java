package com.app.psa;

public class Entry {

	    private String income;
	    private String thisPeriod;
	    private String yearToDate;
	    private String deductions;
	    private String currentTotal;
	    private String yearToDate_;
	    
		public String getIncome() {
			return income;
		}
		public void setIncome(String income) {
			this.income = income;
		}
		public String getThisPeriod() {
			return thisPeriod;
		}
		public void setThisPeriod(String thisPeriod) {
			this.thisPeriod = thisPeriod;
		}
		public String getYearToDate() {
			return yearToDate;
		}
		public void setYearToDate(String yearToDate) {
			this.yearToDate = yearToDate;
		}
		public String getDeductions() {
			return deductions;
		}
		public void setDeductions(String deductions) {
			this.deductions = deductions;
		}
		public String getCurrentTotal() {
			return currentTotal;
		}
		public void setCurrentTotal(String currentTotal) {
			this.currentTotal = currentTotal;
		}
		public String getYearToDate_() {
			return yearToDate_;
		}
		public void setYearToDate_(String yearToDate_) {
			this.yearToDate_ = yearToDate_;
		}
	
}
