package com.app.psa;

public class Benefit {

	    private String name;
	    private String thisPeriod;
	    private String yearToDate;
	    
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getThisPeriod() {
			return thisPeriod;
		}
		public void setThisPeriod(String thisPeriod) {
			this.thisPeriod = thisPeriod;
		}
		public String getYearToDate() {
			return yearToDate;
		}
		public void setYearToDate(String yearToDate) {
			this.yearToDate = yearToDate;
		}
	    
		
	
}
